package com.revature.assignment;

public class Operations {
	int sum;
	int subt;
	int mult;
	int divi;
	
	public int add(int a , int b) {
		sum = a + b;
		return sum;
	}
	public int sub(int a , int b) {
		subt = a - b;
		return subt;
	}
	public int mul(int a , int b) {
		mult = a + b;
		return mult;
	}
	public int div(int a , int b) {
		divi = a + b;
		return divi;
	}

}
